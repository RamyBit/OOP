using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ArrayMinMax
{
    class Vergleich
    {
        static public bool IstKleiner(int a, int b)
        {
            return a < b;
        }
        static public bool IstGroesser(int a, int b)
        {
            return a > b;
        }
    }
}
